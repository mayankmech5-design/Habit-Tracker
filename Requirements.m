3 Mapping of Requirements for Habit Tracking Application 

3.1: User Requirement 1:  “Users shall be able to create daily habits.” 

Related System Requirements 
3.1.1:The system shall allow users to create a new habit with:  

-habit name,description, frequency, reminder time, category  

3.1.2:The system shall store all habit data in the database. 
3.1.3:The application shall load the dashboard within 2 seconds. 
3.1.4:The system architecture shall support future feature additions.  

3.2: User Requirement 2 : “Users shall be able to mark habits as completed.” 
 
Related System Requirements 
3.2.1: The system shall record completion status for each habit daily.  
3.2.2: The system shall display completion percentages visually.  
3.2.3: Habit completion updates shall be processed within 1 second.  
3.2.4: The system availability shall be at least 99%.  
3.2.5: The application shall support intuitive navigation.  

3.3: User Requirement 3 : “Users shall be able to view daily, weekly, and monthly progress.” 

Related System Requirements 
3.3.1: The system shall generate analytics and reports.  

3.4: User Requirement 4 : “Users shall be able to edit or delete habits.”  

Related System Requirements 
3.4.1: The system shall allow users to update existing habits.  
3.4.2: The system shall allow users to delete habits permanently.  

3.5: User Requirement 5 : “Users shall be able to categorize habits (Fitness, Study, Health, etc.).” 

Related System Requirements 
3.5.1: The system shall allow users to create a new habit with category information.  
3.5.2: New users shall understand basic functionality within 10 minutes.  

3.6: User Requirement 6 : “Users shall be able to view streak counts.” 

Related System Requirements 
3.6.1: The system shall track habit completion.  

3.7: User Requirement 7 : “Users shall be able to log in securely.”  

Related System Requirements 
3.7.1: Each user shall have a unique account ID.  
3.7.2: The system shall authenticate users using email and password. 
3.7.3: Passwords shall be stored securely in encrypted form.  
3.7.4: The system shall allow user registration.  
3.7.5: The system shall allow secure login/logout. 
3.7.6: User passwords shall be encrypted.  

3.8: User Requirement 8 : “Users shall be able to synchronize data across devices.” 

Related System Requirements 
3.8.1: The system shall synchronize user data with cloud storage.  
3.8.2: The application shall support Android, iOS, and Web platforms.  
3.8.3: User data shall be backed up daily.  

3.9: User Requirement 9 : “Users shall be able to track habit completion statistics.” 

Related System Requirements 
3.9.1: The system shall generate weekly and monthly progress summaries.  
3.9.2: The system shall calculate streak counts automatically.  

 